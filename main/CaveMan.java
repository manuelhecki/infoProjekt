    public CaveMan() 
    {
        super(20 * 28, 20 * 28, 1);
        GenerateLevel(0);
    }

    public void GenerateLevel(int num)
    {
        Objects = 0;
        int px = TILESIZE/2,
        py = TILESIZE/2;

        removeObjects(getObjects(Block.class));

        for (int i = 0 ; i < 28; i++)
        {     
            for (int j = 0 ; j < 28; j++)
            {
                int ID = LVL_data[num][i][j] ; 

                if (ID == -1)
                {
                    addObject( Player, px, py );
                    Objects++;
                }

                else if(ID > 0)
                {
                    if(ID == 30) {
                        ID = GetManuBlockID(num,i,j);
                    }
                    addObject( new Block(ID), px, py );
                    Objects++;
                }
                px += TILESIZE;
            }           
            px = TILESIZE/2;
            py += TILESIZE;
        }

        maploaded = true;
    }
    
    int GetManuBlockID(int num,int i,int j){
        if(ManuAbove(num,i,j) == true) {
            if(ManuUnder(num,i,j) == true) {
                if(ManuLeft(num,i,j) == true) {
                    if(ManuRight(num,i,j) == true){
                        return 306; 
                    } else{
                        return 307;
                    }
                } else {
                    if(ManuRight(num,i,j) == true){
                        return 305; 
                    } else{
                        return 308;
                    }
                }
            } else {
                if(ManuLeft(num,i,j) == true) {
                    if(ManuRight(num,i,j) == true){
                        return 310;
                    } else{
                        return 311;
                    }
                } else {
                    if(ManuRight(num,i,j) == true){
                        return 309;
                    } else{
                        return 312;
                    }
                }
            }
        } else {
            if(ManuUnder(num,i,j) == true) {
                if(ManuLeft(num,i,j) == true) {
                    if(ManuRight(num,i,j) == true){
                        return 302;  
                    } else{
                        return 303;
                    }
                } else {
                    if(ManuRight(num,i,j) == true){
                        return 301;
                    } else{
                        return 304;
                    }
                }
            } else {
                if(ManuLeft(num,i,j) == true) {
                    if(ManuRight(num,i,j) == true){
                        return 314;
                    } else{
                        return 315;
                    }
                } else {
                    if(ManuRight(num,i,j) == true){
                        return 313;
                    } else{
                        return 316;
                    }
                }
            }
        }
    }
    
    
