    boolean ManuAbove(int num, int i, int j){
        if(i==0) {
            return true; //wenn der block ganz oben ist
        } else if (LVL_data[num][i-1][j] ==30) {
            return true; //wenn ein block drüber ist
        } else {
            return false; //wenn kein block drüber ist lol
        }
    }
